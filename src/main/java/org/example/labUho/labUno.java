package org.example.labUho;

public class labUno {
    public static void main(String[] args) {
        int numUno = 45;
        int numDos = 5;
        System.out.println("La suma de numUno y numDos es " + (numUno + numDos));
        System.out.println("La suma de numUno y numDos es " + (numUno - numDos));
        System.out.println("La suma de numUno y numDos es " + (numUno * numDos));
        System.out.println("La suma de numUno y numDos es " + (numUno / numDos));
    }
}
